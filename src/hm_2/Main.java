public class Main {
    public static void main(String[] args) {

        double milies = 5;
        double kilometers = milies * 1.60934;
        System.out.println(milies + " milies = " + kilometers + " kilometers");

        double kilometersTwo = 15;
        double miliesTwo = kilometersTwo * 0.621371;
        System.out.println(kilometersTwo + " kilometers = " + miliesTwo + " milies");

    }
}
